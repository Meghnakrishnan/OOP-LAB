class Arithmetic {
        public static void main(String args[]) {
        int a,b,add,subtract,multiply,division;		//variable declaration
        a=10;		//assigning value to variable
        b=5; 
        add=a + b;	//add 2 numbers and assign in 'add'
        subtract=a - b;
        multiply=a * b;
        division=a / b;
        
        System.out.println("Sum = " + add); 	//to print results
        System.out.println("Difference = " + subtract);
        System.out.println("Multiplication = " + multiply);
        System.out.println("Division = " + division);
        }
}


/*output

Sum = 15
Difference = 5
Multiplication = 50
Division = 2

*/